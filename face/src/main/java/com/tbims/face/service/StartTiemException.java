package com.tbims.face.service;


public class StartTiemException extends RuntimeException {

	
	private static final long serialVersionUID = 1L;

	public StartTiemException() {
		
	}

	public StartTiemException(String message) {
		super(message);
		
	}

	public StartTiemException(Throwable cause) {
		super(cause);
		
	}

	public StartTiemException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public StartTiemException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
